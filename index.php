<script defer>
(function() {
    'use strict';

    var prefersReduced = window.matchMedia('(prefers-reduced-motion: reduce)').matches;

    // --- Header scroll & Hamburger ---
    var mainHeader = document.getElementById('mainHeader');
    if (mainHeader) {
        window.addEventListener('scroll', function() {
            mainHeader.classList.toggle('scrolled', window.scrollY > 50);
        }, { passive: true });
    }

    var menuToggle = document.getElementById('menuToggle');
    var navMenu = document.getElementById('navMenu');
    if (menuToggle && navMenu && mainHeader) {
        menuToggle.addEventListener('click', function() {
            var active = navMenu.classList.toggle('active');
            menuToggle.classList.toggle('active');
            mainHeader.classList.toggle('menu-open', active);
            menuToggle.setAttribute('aria-expanded', active);
            var icon = menuToggle.querySelector('i');
            if (icon) {
                icon.className = active ? 'fas fa-times' : 'fas fa-bars';
            }
        });

        navMenu.querySelectorAll('a').forEach(function(link) {
            link.addEventListener('click', function() {
                navMenu.classList.remove('active');
                menuToggle.classList.remove('active');
                mainHeader.classList.remove('menu-open');
                menuToggle.setAttribute('aria-expanded', 'false');
                var icon = menuToggle.querySelector('i');
                if (icon) icon.className = 'fas fa-bars';
            });
        });
    }

    // --- Scroll services dots ---
    function initDots(containerId, trackId, dotsId) {
        var container = document.getElementById(containerId);
        var track = document.getElementById(trackId);
        var dotsEl = document.getElementById(dotsId);
        if (!container || !track || !dotsEl) return;
        var cards = track.children;
        if (cards.length < 2) { dotsEl.style.display = 'none'; return; }
        for (var i = 0; i < cards.length; i++) {
            var dot = document.createElement('button');
            dot.className = 'dot' + (i === 0 ? ' active' : '');
            dot.setAttribute('aria-label', 'Slide ' + (i+1));
            dot.dataset.index = i;
            dot.addEventListener('click', function() {
                cards[parseInt(this.dataset.index)].scrollIntoView({ behavior: prefersReduced ? 'auto' : 'smooth', inline: 'center', block: 'nearest' });
            });
            dotsEl.appendChild(dot);
        }
        container.addEventListener('scroll', function() {
            var center = container.getBoundingClientRect().left + container.getBoundingClientRect().width / 2;
            var activeIdx = 0;
            for (var i = 0; i < cards.length; i++) {
                var r = cards[i].getBoundingClientRect();
                if (r.left <= center && r.right >= center) { activeIdx = i; break; }
            }
            Array.from(dotsEl.children).forEach(function(d, idx) { d.classList.toggle('active', idx === activeIdx); });
        }, { passive: true });
    }
    initDots('servicesScroll', 'servicesTrack', 'servicesDots');
    initDots('showcaseScroll', 'showcaseTrack', 'showcaseDots');
    initDots('testimonialScroll', 'testimonialTrack', 'testimonialDots');

    // --- Network tabs ---
    document.querySelectorAll('.network-tab').forEach(function(tab) {
        tab.addEventListener('click', function() {
            document.querySelectorAll('.network-tab').forEach(function(t) { t.classList.remove('active'); t.setAttribute('aria-selected', 'false'); });
            tab.classList.add('active'); tab.setAttribute('aria-selected', 'true');
            document.querySelectorAll('.pricing-table').forEach(function(t) { t.classList.remove('active'); });
            var target = document.getElementById('pricing-' + tab.dataset.network);
            if (target) target.classList.add('active');
        });
    });

    // --- FAQ ---
    document.querySelectorAll('.faq-question').forEach(function(btn) {
        btn.addEventListener('click', function() {
            var item = btn.closest('.faq-item');
            var isActive = item.classList.contains('active');
            document.querySelectorAll('.faq-item').forEach(function(i) { i.classList.remove('active'); i.querySelector('.faq-question').setAttribute('aria-expanded', 'false'); });
            if (!isActive) { item.classList.add('active'); btn.setAttribute('aria-expanded', 'true'); }
        });
    });

    // --- Counters ---
    (function() {
        var counters = document.querySelectorAll('.counter-num');
        if (!counters.length) return;
        var speed = prefersReduced ? 1 : 80;
        var obs = new IntersectionObserver(function(entries) {
            entries.forEach(function(entry) {
                if (entry.isIntersecting) {
                    var c = entry.target;
                    var target = parseInt(c.dataset.target);
                    if (prefersReduced) { c.innerText = target.toLocaleString(); obs.unobserve(c); return; }
                    (function update() {
                        var count = parseInt(c.innerText.replace(/,/g, ''));
                        if (count < target) { c.innerText = Math.ceil(count + target / speed); requestAnimationFrame(update); }
                        else { c.innerText = target.toLocaleString(); }
                    })();
                    obs.unobserve(c);
                }
            });
        });
        counters.forEach(function(c) { obs.observe(c); });
    })();

    // --- Ripple ---
    document.querySelectorAll('.ripple').forEach(function(el) {
        el.addEventListener('click', function(e) {
            if (prefersReduced) return;
            var ripple = document.createElement('span');
            ripple.className = 'ripple-effect';
            var rect = el.getBoundingClientRect();
            var size = Math.max(rect.width, rect.height);
            ripple.style.width = ripple.style.height = size + 'px';
            ripple.style.left = (e.clientX - rect.left - size / 2) + 'px';
            ripple.style.top = (e.clientY - rect.top - size / 2) + 'px';
            el.appendChild(ripple);
            setTimeout(function() { ripple.remove(); }, 600);
        });
    });

    // --- Scroll top ---
    var scrollTop = document.getElementById('scrollTop');
    window.addEventListener('scroll', function() { if (scrollTop) scrollTop.classList.toggle('visible', window.scrollY > 400); }, { passive: true });

    // --- Network status ---
    var statusEl = document.getElementById('networkStatus');
    function showNet(msg, type) {
        if (!statusEl) return;
        statusEl.textContent = msg;
        statusEl.className = 'network-status ' + type + ' visible';
        clearTimeout(statusEl._timer);
        statusEl._timer = setTimeout(function() { statusEl.classList.remove('visible'); }, 3000);
    }
    window.addEventListener('online', function() { showNet('Back online', 'online'); });
    window.addEventListener('offline', function() { showNet('No internet connection', 'offline'); });

    // --- PWA Installation Prompt ---
    var deferredPrompt;
    var installMenuItem = document.getElementById('installMenuItem');
    var pwaInstallBtn = document.getElementById('pwaInstallBtn');

    window.addEventListener('beforeinstallprompt', function(e) {
        e.preventDefault();
        deferredPrompt = e;
        if (installMenuItem) installMenuItem.classList.add('show-pwa');
    });

    if (pwaInstallBtn) {
        pwaInstallBtn.addEventListener('click', function(e) {
            e.preventDefault();
            if (!deferredPrompt) return;
            deferredPrompt.prompt();
            deferredPrompt.userChoice.then(function(choiceResult) {
                if (choiceResult.outcome === 'accepted') {
                    console.log('User accepted PWA installation');
                } else {
                    console.log('User dismissed PWA installation');
                }
                deferredPrompt = null;
                if (installMenuItem) installMenuItem.classList.remove('show-pwa');
            });
        });
    }

    if ('serviceWorker' in navigator) {
        navigator.serviceWorker.register('/sw.js').catch(function() {});
    }
})();
</script>
